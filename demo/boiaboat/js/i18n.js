/* ---- apply translations ---- */
function applyLang(lang) {
  const dict = i18nDict[lang];
  if (!dict) return;

  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] === undefined) return;
    // skip labels that wrap inputs/selects/textareas — update only their first text node
    if (el.tagName === 'LABEL') {
      const firstText = [...el.childNodes].find(n => n.nodeType === 3 && n.textContent.trim());
      if (firstText) firstText.textContent = dict[key];
      return;
    }
    // if GSAP already word-split this element, rebuild word spans with new text
    const words = el.querySelectorAll('.word');
    if (words.length) {
      el.innerHTML = dict[key];
      el.innerHTML = el.textContent.trim().split(' ')
        .map(w => `<span class="word" style="transform:translateY(0)">${w}</span>`).join(' ');
    } else {
      el.innerHTML = dict[key];
    }
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (dict[key] !== undefined) el.setAttribute('placeholder', dict[key]);
  });

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  localStorage.setItem('bb_lang', lang);
}

/* ---- init ---- */
(function(){
  const saved = localStorage.getItem('bb_lang');
  const browserLang = navigator.language.slice(0, 2);
  const defaultLang = ['it','en','de'].includes(saved) ? saved
    : ['it','en','de'].includes(browserLang) ? browserLang
    : 'it';
  applyLang(defaultLang);
})();

/* ---- button listeners (delegated, works for both nav + mobile menu) ---- */
document.addEventListener('click', e => {
  const btn = e.target.closest('.lang-btn');
  if (btn) applyLang(btn.dataset.lang);
});
