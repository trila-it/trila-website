const I18N = {
  current: 'it',

  init() {
    const saved = localStorage.getItem('ls_lang');
    if (saved && (saved === 'it' || saved === 'en')) {
      this.current = saved;
    } else {
      // auto-detect from browser
      const lang = navigator.language || navigator.userLanguage || 'it';
      this.current = lang.startsWith('it') ? 'it' : 'en';
    }
    this.apply();
    this.updateSwitcher();
  },

  set(lang) {
    this.current = lang;
    localStorage.setItem('ls_lang', lang);
    this.apply();
    this.updateSwitcher();
    // update html lang attr
    document.documentElement.lang = lang;
  },

  t(key) {
    return translations[this.current][key] || translations['it'][key] || key;
  },

  apply() {
    document.documentElement.lang = this.current;
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.dataset.i18n;
      const val = this.t(key);
      if (val) el.innerHTML = val;
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.dataset.i18nPlaceholder;
      const val = this.t(key);
      if (val) el.placeholder = val;
    });
    document.querySelectorAll('[data-i18n-href]').forEach(el => {
      // update page links based on lang prefix if needed
    });
  },

  updateSwitcher() {
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === this.current);
    });
  }
};

// Expose globally
window.I18N = I18N;
